# Ein string ist per Definition eine Zeichenkette.
# Die Anzahl der Zeichen ist dabei >= 0. (Größergleich)
kurzerString = ""             # Das ist die Initialisierung eines "leeren strings", also eine Zeichenkette mit 0 Elementen
print(type(kurzerString))     # <class 'str'>

langerString = "Langer Satz mit Leerzeichen und 1 Satzzeichen!"
# Zeichenketten oder strings sind nicht nur auf Buchstaben oder Zahlen limitiert, sondern können auch Sonderzeichen oder Satzzeichen enthalten.
# Achtung: Es gibt einige Sonderzeichen, die für separate Kommdos verwendet werden, z.B. der Backslash '\'
# Um einen Backslash in eine Zeichenkette einzufügen, muss dieser "escaped" werden.
# Für das escapen von Sonderzeichen wird ebenfalls der Backslash verwendet.
test = "This contains \a Backslash"   # '\a' wird als Kommando interpretiert
print(test)                           # This contains \x07 Backslash
test2 = "\\a double Backslash"        # Hier wird der Backslash escaped
print(test2)                          # \\a double Backslash

# strings sind außerdem eine Ausnahme was Variable angeht.
# Normalerweise haben Variablen nur einen Wert, das ist bei strings etwas anders.
# Eigentlich ist ein string eine Liste/Array aller enthaltener Zeichen.
# Deshalb können die Zeichen auch individuell über einen Index addressiert werden.
j = "simple String"
print(j[0])           # 's'
print(j[1])           # 'i'
# ...

